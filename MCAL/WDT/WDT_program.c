#include "LIB/STD_TYPES.h"
#include "LIB/BIT_MATH.h"


#define WDTCR   (*((volatile u8 *)(0x41)))


void WDT_voidInit(void)
{
	// Timeout after 2.1sec
	SET_BIT(WDTCR,2);
	SET_BIT(WDTCR,1);
	SET_BIT(WDTCR,0);
}

void WDT_voidEnable(void)
{
	//Enable WDT
	SET_BIT(WDTCR,3);
}
void WDT_voidDisable(void)
{
	WDTCR = (1<<4)|(1<<3);

	WDTCR = 0x00;

}
